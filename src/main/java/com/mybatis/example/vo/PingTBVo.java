package com.mybatis.example.vo;

public class PingTBVo {
    private int id;
    private int userid;
    private int listingid;
    private int bizid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getListingid() {
        return listingid;
    }

    public void setListingid(int listingid) {
        this.listingid = listingid;
    }

    public int getBizid() {
        return bizid;
    }

    public void setBizid(int bizid) {
        this.bizid = bizid;
    }
}
